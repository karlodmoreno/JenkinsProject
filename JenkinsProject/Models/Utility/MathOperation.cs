using System;
using System.Collections.Generic;
using System.Linq;
using System.Threading.Tasks;

namespace JenkinsProject.Models.Utility
{
    public class MathOperation
    {
        public int Addition(int x, int y)
        {
            return x + y;
        }

        public int Substraction(int x, int y)
        {
            return x - y;
        }
    }
}
